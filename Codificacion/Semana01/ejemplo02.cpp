#include <iostream>
using namespace std;

int main() {
	
	/// DECLARACION DE VARIABLE
	int num1;
	int num2;
	int resultado;
	
	/// INGRESO DE DATOS
	cout << "Ingrese #1: ";
	cin >> num1;
	
	cout << "Ingrese #2: ";
	cin >> num2;
	
	/// SUMAR DOS NUMEROS
	resultado = num1 + num2;
	
	/// LISTAR EL RESULTADO
	cout << "El resultado de " << num1 << " + " << num2 << " es: " << resultado;
	
	
	
	
	return 0;
}

